package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil Penjumlahan 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 8 - 4;
        System.out.println("Hasil Pengurangan 8 - 4 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 3 * 3;
        System.out.println("Hasil Perkalian 3 x 3 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 6 / 2;
        System.out.println("Hasil Pembagian 6 / 2 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Sisa Bagi");
        int hasilSisaBagi =  9 % 3;
        System.out.println("Hasil Sisa Bagi 9 % 3 = " + hasilSisaBagi);
        System.out.println();
    }
}